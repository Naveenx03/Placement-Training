public class Operator
{
	public static void main(String[] args) {
		byte num1 = 100;
		byte num2 = 98;
		byte num3 = (byte)(num1 + num2);
		System.out.println(num3);
		System.out.println(num1 * num2);
		System.out.println(num1 - num2);
		System.out.println(num1 / num2);
		System.out.println(num1 % num2);
		System.out.println(num1 + num2);
	}
}
